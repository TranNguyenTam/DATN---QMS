# Generic clone helper
# ═══════════════════════════════════════════════════════════════════

def clone_table(
    src_conn: pyodbc.Connection,
    src_query: str,
    dst_table: str,
    pk_col: str,
    columns: Sequence[str],
    identity_insert: bool,
    sql_file: SqlFile,
    section_title: str,
    transform=None,
) -> int:
    """
    Generic clone:
      - SELECT rows from src
      - For each row: emit idempotent INSERT to QMS_DA + apply transform
    Returns number of rows actually inserted.
    """
    sql_file.section(section_title)
    cur = src_conn.cursor()
    cur.execute(src_query)
    rows = cur.fetchall()
    print(f"  source: {len(rows)} rows from `{section_title}`")

    if identity_insert:
        sql_file.write(f"SET IDENTITY_INSERT dbo.{dst_table} ON;")
        # Run via dst() too
        dst().execute(f"SET IDENTITY_INSERT dbo.{dst_table} ON;")

    inserted = 0
    skipped = 0
    for r in rows:
        rec = dict(zip(columns, r))
        if transform is not None:
            rec = transform(rec)
            if rec is None:
                continue
        pk_val = rec[pk_col]
        # Build INSERT
        cols_sql = ", ".join(columns)
        vals_sql = ", ".join(sql_literal(rec[c]) for c in columns)
        guard = (
            f"IF NOT EXISTS (SELECT 1 FROM dbo.{dst_table} WHERE {pk_col} = {sql_literal(pk_val)})\n"
            f"  INSERT INTO dbo.{dst_table} ({cols_sql}) VALUES ({vals_sql});"
        )
        sql_file.write(guard)
        # Apply to live DB
        try:
            check = dst().cursor().execute(
                f"SELECT 1 FROM dbo.{dst_table} WHERE {pk_col} = ?", pk_val
            ).fetchone()
            if check:
                skipped += 1
                continue
            placeholders = ", ".join("?" for _ in columns)
            dst().cursor().execute(
                f"INSERT INTO dbo.{dst_table} ({cols_sql}) VALUES ({placeholders})",
                *(rec[c] for c in columns),
            )
            inserted += 1
        except Exception as e:
            print(f"    ! row pk={pk_val} ERROR: {e}")
            sql_file.write(f"-- WARNING: row pk={pk_val} failed locally: {e}")

    if identity_insert:
        sql_file.write(f"SET IDENTITY_INSERT dbo.{dst_table} OFF;\nGO")
        dst().execute(f"SET IDENTITY_INSERT dbo.{dst_table} OFF;")

    sql_file.write("")
    print(f"    inserted={inserted}  skipped(exists)={skipped}")
    return inserted


