    sf = SqlFile(
        "26_seed_dichvu_catalog.sql",
        "Seed dbo.DM_LoaiDichVu + dbo.DM_NhomDichVu + dbo.DM_DichVu (top 500 thông dụng).",
    )

    # 1. DM_LoaiDichVu
