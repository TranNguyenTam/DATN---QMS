def clone_benhnhan() -> tuple[int, int]:
    """Returns (n_bn, n_bhyt)."""
    print("\n[3.1] Clone 200 BN + BHYT records")
    sql_file = SqlFile(
        "20_seed_benhnhan.sql",
        "Seed dbo.BenhNhan + dbo.BenhNhan_BHYT (200 BN, anonymized).",
    )

    # 1) Lấy top 200 BN ID — ưu tiên BN có BHYT
    cur = src_his().cursor()
    cur.execute(
        """
        SELECT TOP 200 bn.BENHNHAN_ID
        FROM PRODUCT_HIS.dbo.TT_BENHNHAN bn
        LEFT JOIN PRODUCT_HIS.dbo.TT_BENHNHAN_BHYT bh ON bn.BENHNHAN_ID = bh.BENHNHAN_ID
        WHERE bn.MAYTE IS NOT NULL AND LEN(bn.MAYTE) > 0
          AND bn.TENBENHNHAN IS NOT NULL
        ORDER BY (CASE WHEN bh.SOTHE IS NOT NULL THEN 0 ELSE 1 END),
                 bn.BENHNHAN_ID DESC
        """
    )
    bn_ids = [r[0] for r in cur.fetchall()]
    print(f"  selected {len(bn_ids)} BN ids")

    if not bn_ids:
        print("  ! no BN found — abort")
        return (0, 0)

    # 2) Load BN records (chỉ cột mình cần)
    bn_cols = (
        "BENHNHAN_ID, MAYTE, SOVAOVIEN, TENBENHNHAN, HO, TEN, GIOITINH, "
        "NGAYSINH, NAMSINH, SODIENTHOAI, DIACHI, DIACHITHUONGTRU, CMND, EMAIL, "
        "ACTIVE, NGAYTAO, NGUOITAO_ID, NGAYCAPNHAT, NGUOICAPNHAT_ID"
    )
    placeholders = ",".join(str(x) for x in bn_ids)
    cur.execute(
        f"SELECT {bn_cols} FROM PRODUCT_HIS.dbo.TT_BENHNHAN WHERE BENHNHAN_ID IN ({placeholders})"
    )
    bn_rows = cur.fetchall()
    bn_col_list = tuple(c.strip() for c in bn_cols.split(","))

    def bn_transform(rec: dict[str, Any]) -> dict[str, Any]:
        rec["CMND"] = anonymize_cmnd(rec.get("CMND"))
        rec["SODIENTHOAI"] = anonymize_phone(rec.get("SODIENTHOAI"))
        # ACTIVE default '1' nếu NULL
        if rec.get("ACTIVE") is None:
            rec["ACTIVE"] = "1"
        return rec

    sql_file.section("BenhNhan (200 rows)")
    n_bn = 0
    skipped_bn = 0
    sql_file.write("SET IDENTITY_INSERT dbo.BenhNhan OFF;  -- BENHNHAN_ID là NOT IDENTITY, không cần\n")
    for r in bn_rows:
        rec = dict(zip(bn_col_list, r))
        rec = bn_transform(rec)
        bn_id = rec["BENHNHAN_ID"]
        cols_sql = ", ".join(bn_col_list)
        vals_sql = ", ".join(sql_literal(rec[c]) for c in bn_col_list)
        sql_file.write(
            f"IF NOT EXISTS (SELECT 1 FROM dbo.BenhNhan WHERE BENHNHAN_ID = {bn_id})\n"
            f"  INSERT INTO dbo.BenhNhan ({cols_sql}) VALUES ({vals_sql});"
        )
        # apply
        try:
            check = dst().cursor().execute(
                "SELECT 1 FROM dbo.BenhNhan WHERE BENHNHAN_ID = ?", bn_id
            ).fetchone()
            if check:
                skipped_bn += 1
                continue
            ph = ", ".join("?" for _ in bn_col_list)
            dst().cursor().execute(
                f"INSERT INTO dbo.BenhNhan ({cols_sql}) VALUES ({ph})",
                *(rec[c] for c in bn_col_list),
            )
            n_bn += 1
        except Exception as e:
            print(f"    ! BN {bn_id} ERROR: {e}")
            sql_file.write(f"-- WARNING: BN {bn_id} failed: {e}")
    sql_file.write("")
    print(f"  BenhNhan inserted={n_bn} skipped={skipped_bn}")

    # 3) BHYT cho 200 BN
    bhyt_cols = (
        "BENHNHAN_BHYT_ID, BENHNHAN_ID, LOAIBHYT, SOTHE, NGAYCAP, NGAYHIEULUC, "
        "NGAYHETHIEULUC, TREN6THANG, TREN3NAM, TREN5NAM, NGAYMIENDONGCHITRA, "
        "KHUVUCSONG_ID, BENHVIEN_KCB_ID, TAMNGUNG, NGAYTAO, NGAYCAPNHAT"
    )
    cur.execute(
        f"SELECT {bhyt_cols} FROM PRODUCT_HIS.dbo.TT_BENHNHAN_BHYT "
        f"WHERE BENHNHAN_ID IN ({placeholders})"
    )
    bhyt_rows = cur.fetchall()
    bhyt_col_list = tuple(c.strip() for c in bhyt_cols.split(","))

    sql_file.section(f"BenhNhan_BHYT ({len(bhyt_rows)} rows) — IDENTITY_INSERT ON")
    sql_file.write("SET IDENTITY_INSERT dbo.BenhNhan_BHYT ON;")
    dst().execute("SET IDENTITY_INSERT dbo.BenhNhan_BHYT ON;")

    n_bhyt = 0
    skipped_bhyt = 0
    for r in bhyt_rows:
        rec = dict(zip(bhyt_col_list, r))
        bh_id = rec["BENHNHAN_BHYT_ID"]
        cols_sql = ", ".join(bhyt_col_list)
        vals_sql = ", ".join(sql_literal(rec[c]) for c in bhyt_col_list)
        sql_file.write(
            f"IF NOT EXISTS (SELECT 1 FROM dbo.BenhNhan_BHYT WHERE BENHNHAN_BHYT_ID = {bh_id})\n"
            f"  INSERT INTO dbo.BenhNhan_BHYT ({cols_sql}) VALUES ({vals_sql});"
        )
        try:
            check = dst().cursor().execute(
                "SELECT 1 FROM dbo.BenhNhan_BHYT WHERE BENHNHAN_BHYT_ID = ?", bh_id
            ).fetchone()
            if check:
                skipped_bhyt += 1
                continue
            ph = ", ".join("?" for _ in bhyt_col_list)
            dst().cursor().execute(
                f"INSERT INTO dbo.BenhNhan_BHYT ({cols_sql}) VALUES ({ph})",
                *(rec[c] for c in bhyt_col_list),
            )
            n_bhyt += 1
        except Exception as e:
            print(f"    ! BHYT id={bh_id} ERROR: {e}")
            sql_file.write(f"-- WARNING: BHYT {bh_id} failed: {e}")

    sql_file.write("SET IDENTITY_INSERT dbo.BenhNhan_BHYT OFF;\nGO\n")
    dst().execute("SET IDENTITY_INSERT dbo.BenhNhan_BHYT OFF;")
    sql_file.save()
    print(f"  BenhNhan_BHYT inserted={n_bhyt} skipped={skipped_bhyt}")
    return (n_bn, n_bhyt)


