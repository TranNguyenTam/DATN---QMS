def sql_literal(v: Any) -> str:
    """Convert a Python value to a T-SQL literal."""
    if v is None:
        return "NULL"
    if isinstance(v, bool):
        return "1" if v else "0"
    if isinstance(v, (int, float)):
        return str(v)
    if isinstance(v, datetime):
        return "'" + v.strftime("%Y-%m-%d %H:%M:%S") + "'"
    # string
    s = str(v).replace("'", "''")
    return "N'" + s + "'"


def anonymize_cmnd(s: str | None) -> str | None:
    if not s:
        return s
    s = str(s).strip()
    if len(s) <= 3:
        return s
    return s[:-3] + "***"


def anonymize_phone(s: str | None) -> str | None:
    if not s:
        return s
    s = str(s).strip()
    if len(s) < 10:
        return s
    # 0901123456 -> 0901****56  (replace 4 middle chars at position 4..8)
    return s[:4] + "****" + s[8:]


