# Generic file writer
# ═══════════════════════════════════════════════════════════════════

class SqlFile:
    """Builds an idempotent SQL seed file."""
    def __init__(self, filename: str, title: str):
        self.path = os.path.join(OUT_DIR, filename)
        self.buf = io.StringIO()
        self.buf.write("-- " + "═" * 64 + "\n")
        self.buf.write(f"-- {filename}\n")
        self.buf.write(f"-- {title}\n")
        self.buf.write(f"-- Auto-generated by _scripts/clone_data.py at {datetime.now().isoformat(timespec='seconds')}.\n")
        self.buf.write("-- Idempotent: chạy lại nhiều lần OK (skip nếu row đã tồn tại).\n")
        self.buf.write("-- " + "═" * 64 + "\n\n")
        self.buf.write("SET QUOTED_IDENTIFIER ON;\nSET ANSI_NULLS ON;\nGO\nUSE QMS_DA;\nGO\n\n")

    def section(self, title: str) -> None:
        self.buf.write(f"-- ─── {title} ─── \n")

    def write(self, sql: str) -> None:
        self.buf.write(sql)
        if not sql.endswith("\n"):
            self.buf.write("\n")

    def save(self) -> None:
        self.buf.write("\nPRINT 'OK: " + os.path.basename(self.path) + "';\nGO\n")
        with open(self.path, "w", encoding="utf-8") as f:
            f.write(self.buf.getvalue())
        print(f"  > wrote {os.path.relpath(self.path, OUT_DIR)}")


