_conns: dict[str, pyodbc.Connection] = {}


def conn(cs: str) -> pyodbc.Connection:
    if cs not in _conns:
        _conns[cs] = pyodbc.connect(cs, timeout=20)
    return _conns[cs]


def src_qms() -> pyodbc.Connection:
    return conn(SRC_QMS_CS)


def src_his() -> pyodbc.Connection:
    return conn(SRC_HIS_CS)


def dst() -> pyodbc.Connection:
    if DST_CS not in _conns:
        c = pyodbc.connect(DST_CS, timeout=20, autocommit=True)
        _conns[DST_CS] = c
    return _conns[DST_CS]


